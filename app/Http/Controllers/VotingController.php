    {
        $user = $request->user();
        $voting = new Voting();

        $voting->user_id = $user->id;
        $voting->choice_id = $request->choice_id;

        $voting->save();

        return response()->json([
            'message' => 'Successfully created voting!'
        ], 201);
    }

